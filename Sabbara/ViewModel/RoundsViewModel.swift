    func getRandomNumber() -> Int? {
        var numbers = Array(0...2)
        
        guard !numbers.isEmpty else {
            return nil
        }
        
        let randomIndex = Int.random(in: 0..<2)
        let randomNumber = numbers[randomIndex]
        
        numbers.remove(at: randomIndex)
        
        return randomNumber
    }

    
    
    
